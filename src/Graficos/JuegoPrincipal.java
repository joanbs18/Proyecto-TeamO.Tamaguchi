    private void tiempoOcultarA(JLabel label, int time) {
        int tiempo = time * 1000;
        Timer timer;
        TimerTask timerTask;
        timerTask = new TimerTask() {

            @Override
            public void run() {
                switch (contaSa) {
                    case 0:
                        contaSa++;
                        label.setVisible(true);
                        break;
                    case 1:
                        contaSa = 0;
                        label.setVisible(false);
                        cancel();
                        break;

                }

            }

        };

        timer = new Timer();
        timer.schedule(timerTask, 0, tiempo);
    }

